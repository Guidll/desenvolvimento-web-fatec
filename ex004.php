<?php
    // Faça um programa que leia uma temperatura em graus Celsius e apresente-a convertida em graus Fahrenheit. A formula de conversão é: F = C∗(9.0/5.0)+32.0, sendo F a temperatura em Fahrenheit e C a temperatura em Celsius
    $celsius = 20;
    $calc = $celsius * ( 9.0 / 5.0 ) + 32.0;
    echo $celsius. " Celsius equivalem a " . $calc. " Fahrenheit";
?>